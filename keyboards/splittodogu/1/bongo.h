void eval_anim_state(bool key_down) {
    switch (anim_state) {
        case Idle:
            // Idle to Tap
            if (key_down) anim_state = Tap;
            break;

        case Prep:
            // Prep to Tap
            if (key_down) {            
                anim_state = Tap;
            
            // Prep to Idle
            } else if (timer_elapsed32(idle_timeout_timer) >= IDLE_TIMEOUT) {
                anim_state = Idle;
                current_idle_frame = 0;
            }
            break;

        case Tap:
            // Tap to Prep
            if (!key_down) {
                anim_state = Prep;
                idle_timeout_timer = timer_read32();
            }
            break;
    }
}

static void draw_bongo(bool key_down) {
    eval_anim_state(key_down);
    oled_set_cursor(0, 0);

    switch (anim_state) {
        case Idle:
            oled_write_raw_P(idle_minimal[abs((IDLE_FRAMES - 1) - current_idle_frame)], ANIM_SIZE);
            if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
                current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
                anim_timer = timer_read32();
            }
            break;

        case Prep:
            oled_write_raw_P(prep_minimal[0], ANIM_SIZE);
            break;

        case Tap:
            oled_write_raw_P(tap_minimal[abs((TAP_FRAMES - 1) - current_tap_frame)], ANIM_SIZE);
            current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
            break;

        default:
            break;
    }
}